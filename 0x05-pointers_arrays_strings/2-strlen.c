#include "main.h"
#include <string.h>

/**
 * _strlen - a function returns the length of a string.
 * @s: a parameter of type char *
 *
 * description: a function that returns the length of a string.
 * Return: type int value, length of the given array
 */

int _strlen(char *s)
{
	unsigned long size = strlen(s);

	return (size);
}
